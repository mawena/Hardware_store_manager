    private void addProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProductButtonActionPerformed
        if(ProductsController.store(new Product(productDesignation.getText(), productDescription.getText(), Double.valueOf(productPrice.getText())))){
            productDesignation.setText("");
            productDescription.setText("");
            productPrice.setText("");
        }
        productTable.setModel(ProductManager.toTableModel(ProductManager.getAll()));
        
    }//GEN-LAST:event_addProductButtonActionPerformed

