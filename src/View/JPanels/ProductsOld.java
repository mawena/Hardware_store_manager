    private void productTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        int myIndex = productTable.getSelectedRow();
        productDesignation.setText(model.getValueAt(myIndex, 1).toString());
        productDescription.setText(model.getValueAt(myIndex, 2).toString());
        productPrice.setText(model.getValueAt(myIndex, 3).toString());
    }//GEN-LAST:event_productTableMouseClicked

    private void clearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearButtonActionPerformed
        productDesignation.setText("");
        productDescription.setText("");
        productPrice.setText("");
    }//GEN-LAST:event_clearButtonActionPerformed

    private void delProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delProductButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        int myIndex = productTable.getSelectedRow();
        ProductsController.destroy(Integer.parseInt(model.getValueAt(myIndex, 0).toString()));
        productTable.setModel(ProductManager.toTableModel(ProductManager.getAll()));
    }//GEN-LAST:event_delProductButtonActionPerformed

    private void updateProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProductButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        int myIndex = productTable.getSelectedRow();
        if(ProductsController.update(Integer.parseInt(model.getValueAt(myIndex, 0).toString()), new Product(productDesignation.getText(), productDescription.getText(), Double.valueOf(productPrice.getText())))){
            productDesignation.setText("");
            productDescription.setText("");
            productPrice.setText("");
        }
        productTable.setModel(ProductManager.toTableModel(ProductManager.getAll()));
    }//GEN-LAST:event_updateProductButtonActionPerformed

