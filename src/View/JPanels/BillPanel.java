    public boolean good() {
        if (OrderId.getSelectedItem().toString().equals("Tout")) {
            JOptionPane.showMessageDialog(null, "Veuillez selectionner un numéro de commande valide!");
            return false;
        } else {
            try {
                Date dateTmp = new SimpleDateFormat("yyyy-MM-dd").parse(((JTextField) DateBill.getDateEditor().getUiComponent()).getText());
                LocalDate currentDate = LocalDate.now();
                LocalDate date = dateTmp.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
                if (currentDate.isBefore(date)) {
                    JOptionPane.showMessageDialog(null, "La date ne peut dépasser le jour actuelle: " + currentDate);
                    return false;
                } else {
                    try {
                        Double.parseDouble(AmoutPaid.getText());
                        return true;
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Le prix doit être un chiffre!");
                        return false;
                    }
                }
            } catch (ParseException ex) {
                return false;
            }
        }
    }

    public void changeDateEntry(String dateEntry) {
        try {
            java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse(dateEntry);   //On formatte correctement la date
            DateBill.setDate(date);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "La date est invalide");
        }
    }

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        disableButton();
        int myIndex = Table.getSelectedRow();
        if (myIndex == -1) {
            JOptionPane.showMessageDialog(null, "Aucune ligne du tableau n'est sélectionné");
        } else {
            DefaultTableModel model = (DefaultTableModel) Table.getModel();
            if (BillsController.destroy(Integer.parseInt(model.getValueAt(myIndex, 0).toString()))) {
                Table.setModel(BillsManager.toTableModel(BillsManager.getAll()));
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        deleteButton.setVisible(true);
        updateButton.setVisible(true);
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        int myIndex = Table.getSelectedRow();
        OrderId.setSelectedItem(model.getValueAt(myIndex, 0).toString());
        AmoutPaid.setText(model.getValueAt(myIndex, 4).toString());
        changeDateEntry(model.getValueAt(myIndex, 7).toString());
    }//GEN-LAST:event_TableMouseClicked

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        disableButton();
        if (good()) {
            if (BillsController.store((double) Integer.parseInt(AmoutPaid.getText()), new Bill(Integer.parseInt(OrderId.getSelectedItem().toString()), ((JTextField) DateBill.getDateEditor().getUiComponent()).getText(), (double) 0) )) {
                Table.setModel(BillsManager.toTableModel(BillsManager.getAll()));
            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

