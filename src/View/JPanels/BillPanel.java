    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        disableButton();
        int myIndex = Table.getSelectedRow();
        if (myIndex == -1) {
            JOptionPane.showMessageDialog(null, "Aucune ligne du tableau n'est sélectionné");
        } else {
            DefaultTableModel model = (DefaultTableModel) Table.getModel();
            if (good()) {
                if (BillsController.update(Integer.parseInt(model.getValueAt(myIndex, 0).toString()), (double) Integer.parseInt(AmoutPaid.getText()), new Bill(Integer.parseInt(OrderId.getSelectedItem().toString()), ((JTextField) DateBill.getDateEditor().getUiComponent()).getText(), (double) 0))) {
                    Table.setModel(BillsManager.toTableModel(BillsManager.getAll()));
                }
            }

        }
    }//GEN-LAST:event_updateButtonActionPerformed

