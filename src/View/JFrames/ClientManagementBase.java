    private void clientTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clientTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) clientTable.getModel();
        int myIndex = clientTable.getSelectedRow();
        clientCNI.setText(model.getValueAt(myIndex, 1).toString());
        clientLastName.setText(model.getValueAt(myIndex, 2).toString());
        clientFirstName.setText(model.getValueAt(myIndex, 3).toString());
        clientTel.setText(model.getValueAt(myIndex, 4).toString());
        clientCategory.setSelectedItem(model.getValueAt(myIndex, 5).toString());
    }//GEN-LAST:event_clientTableMouseClicked

    private void clearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearButtonActionPerformed
        clientCNI.setText("");
        clientLastName.setText("");
        clientFirstName.setText("");
        clientTel.setText("");
    }//GEN-LAST:event_clearButtonActionPerformed

    private void delClientButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delClientButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel) clientTable.getModel();
        int myIndex = clientTable.getSelectedRow();
        ClientController.destroy(Integer.parseInt(model.getValueAt(myIndex, 0).toString()));
        clientTable.setModel(ClientManager.toTableModel(ClientManager.getAll()));
    }//GEN-LAST:event_delClientButtonActionPerformed

